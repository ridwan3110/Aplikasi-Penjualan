    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
int index = tabel_karyawan.getSelectedRow();
if (index != -1){
    KaryawanModel karyawan = tableModelKaryawan.getKaryawan(tabel_karyawan.convertRowIndexToModel(index));
   KaryawanDialog dialog = new KaryawanDialog();
   KaryawanModel updateKaryawanModel = dialog.updateKaryawan(karyawan);
   if (updateKaryawanModel==null){
   karyawanDao.UpdateKaryawan(karyawanModel);
   tableModelKaryawan.UpdateKaryawan(index, karyawan);
    JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
}
   
}
else {
       JOptionPane.showMessageDialog(null, "Seleksi Salah Satu Baris");
   }
                // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
    if (ValidasiInput()==true){
        
        String Nama = txt_nama.getText();
        String Status = (String) cbo_statusKA.getSelectedItem();
        
        karyawanModel = new KaryawanModel();
        karyawanModel.setNama(Nama);
        karyawanModel.setStatus(Status);
        
        karyawanDao.InsertKaryawan(karyawanModel);
        tableModelKaryawan.InsertKaryawan(karyawanModel);
        JOptionPane.showMessageDialog(null, "Data Berhsil Disimpan");
        resetFrm();
    }    
    }//GEN-LAST:event_btn_simpanActionPerformed

