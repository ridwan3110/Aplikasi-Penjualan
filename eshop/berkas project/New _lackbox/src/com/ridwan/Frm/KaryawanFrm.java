    private void BTNHAPUSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNHAPUSActionPerformed
int index = tabel_karyawan.getSelectedRow();
if (index!=-1){
    KaryawanModel karyawan = tableModelKaryawan.getKaryawan(tabel_karyawan.convertRowIndexToModel(index));
    if (JOptionPane.showConfirmDialog(null, "Apakah Anda Ingin Menghapus Data ini", "Konfirmasi", 
            JOptionPane.OK_CANCEL_OPTION)==JOptionPane.OK_OPTION){
        karyawanDao.DeleteKaryawan(karyawanModel);
        tableModelKaryawan.DeleteKaryawan(index);
        JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
    }
}
else {
    JOptionPane.showMessageDialog(null, "Seleksi Salah Satu Baris");
}


    }//GEN-LAST:event_BTNHAPUSActionPerformed

