    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
if (modeEdit==false){
    karyawan = new Karyawan();
}
String username = txt_username.getText();
        char[] pwd = txt_password.getPassword();
        String password = new String (pwd);
        String nama = txt_namakar.getText();
        String status = (String) cbo_status.getSelectedItem();
        String telepon = txt_telponkar.getText();
        String alamat = txt_alamatkar.getText();
        
        if (password.trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nama masih kosong");
        }
        else if (username.trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Username Masih kosong");
        }
        else if (nama.trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nama masih kosong");
        }
        else if (status.trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Status belum dipilih");
        }
        else if (telepon.trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Telepon Masih Kosong");
        }
        else if (alamat.trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Alamat Masih Kosong");
        }
        else {
           karyawan.setUsername(nama);
            karyawan.setPassword(password);
            karyawan.setNama(nama);
            karyawan.setStatus(status);
            karyawan.setTelepon(telepon);
            karyawan.setAlamat(alamat);
            dispose();
            
        }
        
        
    }//GEN-LAST:event_bSimpanActionPerformed

