       this.modeEdit=true;
       txt_username.setText(parameter.getUsername());
       txt_password.setText(parameter.getPassword());
       txt_namakar.setText(parameter.getNama());
       cbo_status.setSelectedItem(parameter.getStatus());
       txt_telponkar.setText(parameter.getTelepon());
