    private void loadGudang(){
        List<Gudang> gudang = gudangDao.getGudangs();
        for (Gudang g: gudang){
            cbo_gudang.addItem(g);
        }
    }
    
    public Barang insertBarang(){
        setTitle("Tambah Barang");
        setLocationRelativeTo(null);
        this.modeEdit=false;
        setVisible(true);
        return barang;
    }
    
    public Barang updateBarang(Barang ba){
        this.barang=ba;
        this.modeEdit=true;
        loadGudang();
        txt_namabar.setText(ba.getNama());
        txt_hargabel.setText(String.valueOf(ba.getHargabeli()));
        txt_hargaju.setText(String.valueOf(ba.getHargajual()));
        txt_jumlah.setText(String.valueOf(ba.getJumlah()));
        txt_tanggal.setDate(ba.getTanggal());
        cbo_gudang.setSelectedItem(ba.getGudang());
        
        setLocationRelativeTo(null);
        setVisible(true);
        return barang;
        
        
    }

