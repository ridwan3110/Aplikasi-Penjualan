    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
     if (modeEdit==false){
         barang = new Barang();
     }
     
  
        if (txt_namabar.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "nama Masih kosoang");
        }
        else if (txt_hargabel.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Harga beli Masih Kosong");
        }
        else if (txt_hargaju.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Harga jual masih kosong");
        }
        else if (txt_jumlah.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Jumlah masih kosong");
        }
        else if (txt_tanggal.getDate()==null){
            JOptionPane.showMessageDialog(null, "Tanggal masih kosong");   
        }
        else if (cbo_gudang.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null, "Gudang masih kosong");
        }
        else {
              String nama = txt_namabar.getText();
        Double hargabeli = Double.valueOf(txt_hargabel.getText());
        Double hargajual = Double.valueOf(txt_hargaju.getText());
        int jumlah = Integer.valueOf(txt_jumlah.getText());
        Date tanggal = txt_tanggal.getDate();
        Gudang gudang = (Gudang) cbo_gudang.getSelectedItem();
        
            barang.setNama(nama);
            barang.setHargabeli(hargabeli);
            barang.setHargajual(hargajual);
            barang.setJumlah(jumlah);
            barang.setTanggal(tanggal);
            barang.setGudang(gudang);
            dispose();
        }
    
    }//GEN-LAST:event_btnSimpanActionPerformed

