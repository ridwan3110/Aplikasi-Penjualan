    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       if (validasiInput()==true){
           transaksi= new Transaksi();
           transaksi.setTransaksiId(Integer.valueOf(txt_KodeTrx.getText()));
           transaksi.setPpn(Double.valueOf(txt_ppn.getText()));
           transaksi.setTanggal(txt_tanggal.getDate());
           Karyawan k = (Karyawan) cbo_karyawan.getSelectedItem();
           transaksi.setKaryawan(k);
           
           //lakukan proses perulangan untuk mengisi list barang detil;
           List<BarangTransaksi> list = new ArrayList<>();
           for (int i=0; i<tabelbarangdetil.getRowCount();i++){
               BarangTransaksi bt = new BarangTransaksi();
               bt.setTransaksi(transaksi);
               
               Barang b = (Barang) tabelbarangdetil.getValueAt(i, 0);
               int jumlah = (int) tabelbarangdetil.getValueAt(i, 1);
               Double harga = (Double) tabelbarangdetil.getValueAt(i, 2);
               bt.setBarang(b);
               bt.setJumlah(jumlah);
               bt.setHarga(harga);
               list.add(bt);
           }
           
           transaksi.setBarangTransaksi(list);
           //lakukan proses insert transaksi
           if (transaksiDao.insertTransaksi(transaksi)==true){
               JOptionPane.showMessageDialog(null, "Data berhasil Disimpan");
               listtransaksi.add(transaksi);
               resetFrm();
           }else {
               JOptionPane.showMessageDialog(null, "Data gagal disimpan");
           }
       }
    }//GEN-LAST:event_jButton3ActionPerformed

