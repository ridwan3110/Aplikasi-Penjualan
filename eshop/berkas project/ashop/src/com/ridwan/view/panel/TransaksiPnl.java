    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            TransaksiInterface dao = new TransaksiDao();
            List<ReportTransaksi> transaksi1 = dao.getTransaksi(transaksi);
            HashMap map = new HashMap();
            map.put("SubTitle", "Berdasarkan ID Transaksi"+transaksi.getTransaksiId());
            JasperPrint jp = JasperFillManager.fillReport(this.getClass().getClassLoader().getResourceAsStream
                ("com/ridwan/Laporan/LaporanRpt.jasper"), map, new JRBeanCollectionDataSource(transaksi1));
            JRViewer jrv = new JRViewer(jp);
            TampilReport report = new TampilReport("Laporan Transaksi berdasarkan Id", jrv);
        } catch (JRException ex) {
            Logger.getLogger(TransaksiPnl.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

