    private boolean validasiInput(){
        boolean valid = false ;
        if(txt_KodeTrx.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Kode Masih Kosong");
        }
        else if (txt_ppn.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "PPN masih kosong");
        }
        else if (txt_tanggal.getDate()==null){
            JOptionPane.showMessageDialog(null, "Tanggal masih kosong");
        }
        else if (cbo_karyawan.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null, "Karyawan belum dipillih");
        }
        else if (tabelbarangdetil.getRowCount()==0){
            JOptionPane.showMessageDialog(null, "Tabel barang detil belum ada");
        }
        else {
            valid = true;
        }
        return  valid;
    }
    
    
